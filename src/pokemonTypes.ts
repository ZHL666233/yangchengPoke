// 计算攻击属性对防御属性组合的总倍率
export const getTypeEffectiveness = (attackType: string, defenseTypes: string[]): { multiplier: number, label: string } => {
  let total = 1;
  for (const defType of defenseTypes) {
    const chart = TYPE_CHART[attackType];
    if (chart && chart[defType] !== undefined) {
      total *= chart[defType];
    }
  }
  if (total >= 2) return { multiplier: total, label: '效果拔群' };
  if (total === 0) return { multiplier: total, label: '无效' };
  if (total < 1) return { multiplier: total, label: '效果不佳' };
  return { multiplier: total, label: '普通效果' };
};

// 获取某个防御属性面对所有攻击属性的克制关系
export const getDefensiveTypeChart = (defenseTypes: string[]): { attackType: string; multiplier: number }[] => {
  const allTypes = ['一般', '火', '水', '草', '电', '冰', '毒', '地面', '飞行', '超能力', '虫', '岩石', '幽灵', '龙', '恶', '钢', '妖精', '格斗'];
  const results: { attackType: string; multiplier: number }[] = [];
  for (const atkType of allTypes) {
    const { multiplier } = getTypeEffectiveness(atkType, defenseTypes);
    if (multiplier !== 1) {
      results.push({ attackType: atkType, multiplier });
    }
  }
  results.sort((a, b) => a.multiplier - b.multiplier);
  return results;
};
